function ans = uiViewPanelGetDflt(request)
% service function to get some default values for 
% the view panel

% possible requests : 'minWinDim' -> minimal window dimension
%                     'winTitleHeight' -> height of the title bar

minWinDim = [400, 220];
winTitleHeight = 50;


switch(request)
case 'minWinDim',
   ans = minWinDim;
case 'winTitleHeight'
	ans = winTitleHeight;   
end;

